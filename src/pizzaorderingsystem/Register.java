    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String FirstName1 = FirstName.getText();
        String LastName1 = LastName.getText();
        String UserName1 = Username.getText();
        String Password1 = Password.getText();
        String Checker = jTextField1.getText();
        String Phone1 = Phone.getText();
        File file = new File("C:\\Users\\Asus\\Downloads\\Compressed\\PizzaOrderingSystem\\src\\pizzaorderingsystem\\Registry.txt");
        try {
            if (Firstname(FirstName1) == false || Lastname(LastName1) == false || Phone(Phone1) == false
                    || Username(UserName1) == false || Password(Password1) == false || Checker.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, errors);
                errors.delete(0, errors.length());
            } else {
                if (Checker.equals(Password1)) {
                    BufferedWriter write = new BufferedWriter(new FileWriter(file, true));
                    write.write(FirstName1 + ";");
                    write.write(LastName1 + ";");
                    write.write(Phone1 + ";");
                    write.write(UserName1 + ";");
                    write.write(Password1 + ";");
                    write.write("0");
                    write.newLine();
                    write.close();
                    JOptionPane.showMessageDialog(rootPane, "You are now Registered! Proceed to Log In");
                    this.setVisible(false);
                    new MainMenu().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Confirmation Password does not match");
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Invalid Input");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

