    public boolean Firstname(String FirstName1) {
        if (FirstName1.isEmpty() || FirstName1.equals("First Name")) {
            errors.append("First Name field is Empty\n");
            return false;
        } else if (Pattern.matches("[a-zA-Z]+\\ [a-zA-Z]+|[a-zA-Z]+", FirstName1) == false) {
            errors.append("First Name field should only contain Letters and Without Spaces\n");
            return false;
        } else {
            return true;
        }
    }

    public boolean Lastname(String LastName1) {
        if (LastName1.isEmpty() || LastName1.equals("Last Name")) {
            errors.append("Last Name field is Empty\n");
            return false;
        } else if (Pattern.matches("[a-zA-Z]+|[a-zA-Z]+\\s[a-zA-Z]+", LastName1) == false) {
            errors.append("Last Name field should only contain Letters and Without Spaces\n Note: If you have Suffixes, you may only use one space and type in your Suffix");
            return false;
        } else {
            return true;
        }
    }

    public boolean Username(String UserName1) {
        if (UserName1.isEmpty() || UserName1.equals("Username")) {
            errors.append("Username is Empty\n");
            return false;
        } else if (Pattern.matches("[A-Za-z0-9+]{1,10}", UserName1) == false) {
            errors.append("Username should only have 10 characters\n");
            return false;
        } else {
            return true;
        }
    }

    public boolean Phone(String Phone1) {
        if (Phone1.isEmpty() || Phone1.equals("Phone")) {
            errors.append("Phone Field is Empty\n");
            return false;
        } else if (Pattern.matches("(^0|\\+63)[0-9+]{1,12}|[0-9+]{1,7}", Phone1) == false) {
            errors.append("Phone numbers should only contain either a 7 digit landline number or an 11 digit Mobile Number and Starts with either 0 or +63");
            return false;
        } else {
            return true;
        }
    }

    public boolean Password(String Password1) {
        if (Password1.isEmpty() || Password1.equals("Password")) {
            errors.append("Password Field is Empty");
            return false;
        } else if (Pattern.matches("[A-Za-z0-9+]{7,10}", Password1) == false) {
            errors.append("Password should contain only 7 characters and does not contain Special Characters");
            return false;
        } else {
            return true;
        }
    }

